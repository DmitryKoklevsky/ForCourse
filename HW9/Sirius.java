package core;

public class Sirius  extends Star{

    protected String typeStar;
    protected int Radius;

    public String getTypeStar() {
        return typeStar;
    }

    public void setTypeStar(String typeStar) {
        this.typeStar = typeStar;
    }

    public int getRadius() {
        return Radius;
    }

    public void setRadius(int radius) {
        Radius = radius;
    }

}
